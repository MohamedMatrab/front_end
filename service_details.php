            <p class="fst-italic">
            La pose d’un implant est souvent mise en concurrence avec les bridges fixes qui s’appuient sur les dents naturelles. Il présente cependant beaucoup plus d’avantages, car c’est une solution parfaitement autonome. Un implant permet non seulement de remplacer une dent manquante, mais augmente en plus, indirectement, la durée de vie des dents avoisinantes. En effet, le remplacement d’une dent par un implant n’implique aucune mutilation (taille et dévitalisation) des dents adjacentes (dents présentes de chaque côté de la dent extraite). Ces mutilations sont généralement observées lorsqu’un bridge fixé sur des dents naturelles est mis en place, conduisent souvent à des complications qui écourtent la durée de vie des dents piliers et donc des bridges. Par ailleurs, un problème (carie, fracture, déchaussement, abcès…) qui viendrait à toucher une dent adjacente à l’implant serait sans incidence sur celui-ci; a contrario un problème qui toucherait l’implant serait sans incidence sur les dents adjacentes.
