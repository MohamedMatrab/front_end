                <h2 >Quelques techniques dentaires esthétiques :</h2>


                <h5><b>Collage dentaire :</b></h5>

                <p>le fait de réparer les dents qui peuvent être cassées, écaillées, décolorées ou déminéralisées avec du simple composite ou des fines couches de céramiques placées sur les faces de la dent abîmée appelée facettes.</p>


                <h5><b>Couronnes :</b></h5>

                <p>Ce sont des coiffes qui recouvrent entièrement les dents endommagées par la carie ou cassées ou encore fissurées, elles sont en céramique ou zirconium.</p>


                <h5><b>Implant dentaire :</b></h5>

                <p>C’est un remplacement total de la dent racine et couronne.</p>

                <h5><b>Bridge dentaire :</b></h5>

                <p>Comme son nom l’indique, c’est un pont collé sur les dents, prenant appui sur les dents adjacentes pour combler les espaces vides. Il peut être tout en céramique ( zirconium ) ou bien céramo métallique</p>
            
  
